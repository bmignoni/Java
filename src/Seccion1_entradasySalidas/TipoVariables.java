
package Seccion1_entradasySalidas;

public class TipoVariables {
    
    public static void main(String[] args) {
        byte entero = 12;
        long entero2 = 58;
        float decimal = 3.45f;
        double decimal2 = 3.45;
        char caracter = 'a';
        boolean decision = true;
        
        
        // Una variable es un espacio de memoria donde se almacenara un dato (byte, float, double, long, string, etc)
        System.out.println("Estos son tipos de variables");
        System.out.println("El tipo byte entero es: " + entero);
        System.out.println("El tipo long entero es: " + entero2);
        System.out.println("El tipo float decimal es: " + decimal);
        System.out.println("El tipo decimal decimal es: " + decimal2);
        System.out.println("El tipo char caracter es: " + caracter);
        System.out.println("El tipo boolean es: " + decision);
    
    
    }
}
